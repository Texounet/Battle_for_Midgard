#include "../header/main.h"

void choix_menu(SDLKey touche)
{
    if(touche == SDLK_s)
    {
        if(choix_att < 3)
            choix_att++;
    }
    if(touche == SDLK_z)
    {
        if(choix_att > 0)
            choix_att--;
    }
}

